Yportal2=0
Yportal4=0
def acharPortais(MAZE):
    global portais, Yportal2, Yportal4

    for y in range(len(MAZE)):
        for x in range(len(MAZE[y])):
            if MAZE[y][x] == "0":
                if len(portais)<5:
                    portais.append((y, x))
                    if len(portais)==2:
                        Yportal2=y+1
                    if len(portais)==4:    
                        Yportal4=y+1
    return portais
    
