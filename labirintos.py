portais=[]
portal2=0
def acharPortais(MAZE):
    global portais
    global portal2
    for y in range(len(MAZE)):
        for x in range(len(MAZE[y])):
            if MAZE[y][x] == "0":
                portais.append((y, x))
                if len(portais)==2:
                    portal2=y+1
    return portais

def move(dy,dx,MAZE):
    global posX, posY
    new_y = posY + dy
    new_x = posX + dx
        
    if MAZE[new_y][new_x] in [" ", "S", "0"]:
        MAZE[posY][posX] = " "
        posY, posX = new_y, new_x
        
    if MAZE[posY][posX] == "0":
        portais = acharPortais(MAZE)
            
        if len(portais) == 2:
            if (posY, posX) == portais[0]:
                portais[1]=(portal2, portais[1][1])
                posY, posX = portais[1]
            else:
                posY, posX = portais[0]
    MAZE[posY][posX]="@"

